package com.interview;

public class Triangle {
	
	public static void main (String args[])
	{
		int n=5, k=n*2-2;
		
		for (int i=0;i<n;i++)
		{      
			 
			
			for(int j=0;j<k;j++)
			{
				System.out.println(" ");
				
				
			}
			
			k=k-1;
			for(int j=0;j<=i;j++)
			{
				System.out.print("* ");
			}
			System.out.println();
			
		}
		
		
		
		
	}

}
